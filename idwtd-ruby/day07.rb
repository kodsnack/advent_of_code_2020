$map = {}

input.strip.lines.map(&:strip).each do |line|
  match = line.match(/^(.+?) bags contain (.+?)\.$/)
  color, contents = match.captures
  $map[color] ||= []
  if contents != "no other bags"
    contents.split(", ").each do |subentry|
      submatch = subentry.match(/^(\d+) (.+?) bags?$/)
      count, subcolor = submatch.captures
      $map[color] << [subcolor, count.to_i]
    end
  end
end

def contains_color(containing, searching)
  entries = $map[containing]
  entries.each do |subcolor, count|
    if subcolor == searching
      return true
    elsif contains_color(subcolor, searching)
      return true
    end
  end
  false
end

puts $map.keys.count { |c| contains_color(c, "shiny gold") }

def count_bags(color)
  entries = $map[color]
  total = 0
  entries.each do |subcolor, count|
    total += count
    total += count_bags(subcolor) * count
  end
  total
end

