int main(int argc, const char * argv[]) {
    // So, we basically need to first convert FBFF.. to binary :)
    // B is binary 1. F is binary 0. R is binary one. L is binary 0 :)
    std::basic_istringstream<char> in {pData};
    std::string sEntry;
    std::vector<int> seat_codes {};
    while (in >> sEntry) {
        int seat_code = std::accumulate(sEntry.begin(), sEntry.end(), int(0),[](int result,char ch) {
            return result*2 + ((ch=='B' or ch=='R')?1:0);
        });
        std::cout << "\n" << sEntry << " = seat_code:" << seat_code << " | " << std::bitset<10>(seat_code).to_string();
        std:: cout << " Seat ID:" << (seat_code & 0b1111111000);
        std::cout << " Row:" << (seat_code / 8) << " Column:" << (seat_code % 8);
        seat_codes.push_back(seat_code);
    }
    // It would seem the column is (seat_code div 8) and the row is (seat_code mod 8)?
    // Let's make sure :)
    // YES!
    std::sort(seat_codes.begin(), seat_codes.end());
    std::cout << "\n\nHighest Seat ID = " << seat_codes.back();
    
    std::cout << "\n\n";
    return 0;
}
